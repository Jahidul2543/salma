package controlflow;

public class IncDec {
    public static void main(String args[]){
        int a = 2;
        int b = 4;
        int c;
        int d;
        c = --b;
        d = ++a;
        c++;
        System.out.println(" a=" +a);
        System.out.println(" b =" +b--);
        System.out.println(" c =" +c++);
        System.out.println(" d =" + a);



    }
}
